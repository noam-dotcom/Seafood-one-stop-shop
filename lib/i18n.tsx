// ── Context ──────────────────────────────────────────────────────────

interface LanguageContextType {
  lang: LangCode
  setLang: (lang: LangCode) => void
  t: (key: string, vars?: Record<string, string>) => string
  dir: 'ltr' | 'rtl'
}

const LanguageContext = createContext<LanguageContextType>({
  lang: 'en',
  setLang: () => {},
  t: (key) => key,
  dir: 'ltr',
})

// Increment this to force-reset all users' saved language to English
const LANG_STORAGE_VERSION = '2'

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<LangCode>('en')

  // Load from localStorage on mount, with version-based reset
  useEffect(() => {
    try {
      const version = localStorage.getItem('seahub-lang-v')
      if (version !== LANG_STORAGE_VERSION) {
        // Version mismatch → clear old saved language, reset to English
        localStorage.removeItem('seahub-language')
        localStorage.setItem('seahub-lang-v', LANG_STORAGE_VERSION)
        setLangState('en')
        return
      }
      const saved = localStorage.getItem('seahub-language') as LangCode | null
      if (saved && SUPPORTED_LANGUAGES.find(l => l.code === saved)) {
        setLangState(saved)
      }
    } catch {
      // localStorage not available, stay at default 'en'
    }
  }, [])

  const setLang = useCallback((newLang: LangCode) => {
    setLangState(newLang)
    try {
      localStorage.setItem('seahub-language', newLang)
      localStorage.setItem('seahub-lang-v', LANG_STORAGE_VERSION)
    } catch { /* ignore */ }
  }, [])

  const langMeta = SUPPORTED_LANGUAGES.find(l => l.code === lang)!
  const dir = langMeta?.dir ?? 'ltr'

  // Update <html> attributes
  useEffect(() => {
    document.documentElement.lang = lang
    document.documentElement.dir = dir
  }, [lang, dir])

  const t = useCallback((key: string, vars?: Record<string, string>): string => {
    const dict = TRANSLATIONS[lang]
    let str = dict?.[key] ?? TRANSLATIONS.en[key] ?? key
    if (vars) {
      Object.entries(vars).forEach(([k, v]) => {
        str = str.replace(`{${k}}`, v)
      })
    }
    return str
  }, [lang])

  const ctxValue = useMemo(
    () => ({ lang, setLang, t, dir }),
    [lang, setLang, t, dir]
  )

  return (
    <LanguageContext.Provider value={ctxValue}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  return useContext(LanguageContext)
}

export function useT() {
  return useContext(LanguageContext).t
}
